# Output which letter occurred the most 
most_occurred = ""
least_occurred = ""
highest_count=0
lowest_count=1_000 
for key,value in sorted (letter_counts.items()):
    if value > highest_count:
        highest_count = value
        most_occurred = key
    elif value < lowest_count:
        lowest_count = value
        least_occurred = key
    else:
        continue

